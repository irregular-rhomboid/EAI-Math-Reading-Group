# ╔═╡ a8f612b1-fdcb-463e-add4-972b7f178b08
function random_wigner(n, σ=1.0)
	H = randn(n,n)
	return Symmetric((H+H') * σ / sqrt(2*n))
end

# ╔═╡ 2bed026f-0e69-4d5f-b624-c4e2f9e5adf5
function random_symmetric_orthogonal(n)
	W = random_wigner(n)
	eig = eigen(W)
	λ = Diagonal(sign.(eig.values))
	return Symmetric(eig.vectors * λ * eig.vectors')
end

# ╔═╡ 3774764c-b6fe-4749-9b9a-fd245519ecea
function orthogonal_projector(M)
	return (M+I)/2
end

