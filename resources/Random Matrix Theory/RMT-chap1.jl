# ╔═╡ cce96a7e-a401-4cdd-abb4-65d20daeedff
md"""
By the Perron-Frobenius theorem, the dominant eigenvalue ``\lambda_\max`` of a positive matric ``A`` is bounded above by ``\max_i \sum_j A_{ij}``.

By the Gershgorin theorem, we already know that the spectral radius is smaller than ``\max_i |A_{ii}| + R_i = \max_i \sum_j |A_{ij}|``, which yields the desired bound under the hypotheses of the Perron-Frobenius theorem.
"""

# ╔═╡ fd7c00fa-5fa1-46b0-8749-e82bd55e39c3
md"""
## Exercise 1.2.2 Sherman-Morrison
By multiplying the right hand side with ``(A + uv')`` we obtain

```math
\begin{align*}
&\left( A^{-1} - \frac{A^{-1}uv' A^{-1}}{1 + v' A^{-1} u}\right) (A + uv') \\
&= I + A^{-1}uv' - \frac{A^{-1}uv'}{1 + v' A^{-1} u} + \frac{A^{-1}u(v' A^{-1}u)v'}{1 + v' A^{-1} u} \\
&= I + A^{-1}uv' -  \frac{(1+v'A^{-1}u)A^{-1}uv'}{1+ v'A^{-1}u} \\
&= I

\end{align*}
```
"""

