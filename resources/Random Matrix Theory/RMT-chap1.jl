3. The matrix $P_+$ is defined as $P_+ = \frac{1}{2}(M + I)$ Convince yourself that $P_+$ is the projector onto the eigenspace of $M$ with eigenvalue $1$. Explain the effect of the matrix $P_+$ on eigenvectors of $M$.

This can easily be seen by taking the eigenvalue decomposition of $M$:
```math
M = O \begin{bmatrix} I_+ & \\ & -I_- \end{bmatrix} O',
```
where $O$ is an orthonormal basis of eigenvectors, and $I_+$ and $I_-$ are identity matrices of appropriate size. Then
```math
\frac{1}{2}(M+I) = \frac{1}{2} O \begin{bmatrix} 2I_+ & \\ & 0 \end{bmatrix} O' = O \begin{bmatrix} I_+ & \\ & 0 \end{bmatrix} O'
```
which is the orthogonal projector onto the eigenspace associated with the eigenvalue $1$.
