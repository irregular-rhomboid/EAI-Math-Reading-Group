md"""
## Exercise 1.2.3 Combining Schur and Sherman-Morrison

Let $n=1$ in Schur's complement formula. Use the Sherman-Morrisson formula to show in that case that 
```math
Q_{22} = (M_{22})^{-1} + \frac{(M_{22})^{-1} M_{21}M_{12}(M_{22})^{-1}}{M_{11} + M_{12}(M_{22})^{-1} M_{21}}
```

By definition, we have by writing the product of $M$ and $M^{-1}$ block by block that
```math
\begin{align}
M_{11}Q_{11} + M_{12}Q_{21} &= 1, \\
M_{21}Q_{12} + M_{22}Q_{22} &= I, \\
M_{11}Q_{12} + M_{12}Q_{22} &= 0, \\
M_{21}Q_{11} + M_{22}Q_{21} &= 0,
\end{align}
```
from which we obtain after some manipulations
```math
Q_{12} = -M_{12}Q_{22} / M_{11}.
```
We can inject this in the second equation to obtain 
```math
\left(M_{22} - \frac{M_{21}M_{12}}{M_{11}} \right)Q_{22} = I,
```
i.e. $Q_{22} = \left(M_{22} - \frac{M_{21}M_{12}}{M_{11}} \right)^{-1}$, so by the Sherman-Morisson formula
```math
\begin{align}
Q_{22} &= (M_{22})^{-1} + \frac{(M_{22})^{-1} \frac{M_{21} M_{12}}{M_{11} (M_{22})^{-1}} }{1 + \frac{1}{M_{11}} M_{12}(M_{22})^{-1} M_{21}}, \\
&= (M_{22})^{-1} + \frac{(M_{22})^{-1} M_{21} M_{12} (M_{22})^{-1} }{M_{11} +  M_{12}(M_{22})^{-1} M_{21}},
\end{align}
```
"""

